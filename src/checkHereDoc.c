int checkHereDoc(int i, t_msh *msh)
{
	int tmpFd;

	if (ft_strcmp(msh->cmd[i + 1], "<<")) {
		if (msh->cmd[i + 2] == NULL) {
			write(2, "syntax error near unexpected token `newline`\n",
				  ft_strlen("syntax error near unexpected token `newline`\n"));
			return (-1);
		}
		return (getTmpFile(msh->cmd[i + 2]));
	}
	return (0);
}